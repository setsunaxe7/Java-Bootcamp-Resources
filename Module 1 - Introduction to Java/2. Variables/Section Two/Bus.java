public class Bus {
    public static void main(String[] args) {
        //Lesson 2: Update passengers variable
        int passengers = 0;
        passengers += 9;
        System.out.println("Number of passengers: " + passengers);

        passengers -= 5;
        System.out.println("Number of passengers: " + passengers);

        passengers -= 4;
        System.out.println("Number of passengers: " + passengers);

        // += operator: updates by adding value on right to variable
        // -= operator: updates by subtracting value on right from variable
    }
    
}
