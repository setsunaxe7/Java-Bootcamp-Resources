// main() is the entry point of the program
// println() prints message to console
// every println() call outputs a new line
// Inside "" is the printed message
// Semicolon ; ends the statement
// Camelcase is used for naming classes and methods (See HelloJava and main)
// Curly braces {} define the start and end of classes and methods
// Public means accessible from other classes
// Static means it belongs to the class, not instances
// Void means it does not return any value
// System is a built-in class
// out is a static member of System class
// println is a method of the out object
// args is an array of command-line arguments

